    public static function get(string $caracteristic)
    {
        if (!isset(self::CARACTERISTICS_MAP[$caracteristic])) {
            throw new \InvalidArgumentException("Caractéristique inconnue: {$caracteristic}");
        }

        $type = self::CARACTERISTICS_MAP[$caracteristic];
        return self::CARACTERISTICS_CONFIG[$type];
    }

    public static function check(string $caracteristic, int $value, int $level = null): bool
    {
        $config = self::get($caracteristic);

        if ($level) {
            return $value >= $config['classe']['niveaux'][$level]['min']
                && $value <= $config['classe']['niveaux'][$level]['max']['total'];
        }

        return $value >= $config['classe']['min']
            && $value <= $config['classe']['max']['total'];
    }

    public static function render(string $caracteristic, int $value, int $level = null): int
    {
        if (!self::check($caracteristic, $value, $level)) {
            $config = self::get($caracteristic);
            return $level
                ? $config['classe']['niveaux'][$level]['max']['total']
                : $config['classe']['max']['total'];
        }
        return $value;
    }
